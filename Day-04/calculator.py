

def add(num1, num2):
    res = num1 + num2
    return res

def subtract(num1, num2): 
    res = num1 - num2
    return res

def multiply(num1, num2):
    res = num1 * num2
    return res

def divide(num1, num2):
    res = num1 // num2
    return res

print(add(10, 5))
print(subtract(10, 5))
print(multiply(10, 5))
print(divide(10, 5))